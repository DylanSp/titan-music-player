
package com.titanplayer.bll;

public class Song {
    
    String title, artist;

    public Song(String newTitle, String newArtist) {
        title = newTitle;
        artist = newArtist;        
    }

    public String getTitle() {
        return title;
    }

    public String getArtist() {
        return artist;
    }
    
    
    
    
    
}
